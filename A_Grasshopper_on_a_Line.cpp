#include <bits/stdc++.h>
using namespace std;
using ll = long long;
    
int main() {
    ios::sync_with_stdio(false);
    
    ll t;
    cin>>t; 
    while(t--){
        ll x,k;
        cin>>x>>k;

        if (x % k == 0) 
        {
            cout << 2 << endl; 
            cout << 1 << " " << x - 1 << endl; 
        }
        else 
        {
            cout << 1 << endl; 
            cout << x << endl; 
        }
    }
    
    return 0;
}